const fullDict: Record<Lang, Strings> = { en, sw, so };

const I18nContext = createContext<{ lang: Lang; setLang: (l: Lang) => void; t: Strings }>({
  lang: "en", setLang: () => {}, t: en,
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");
  useEffect(() => {
    const saved = (typeof window !== "undefined" && (localStorage.getItem("fursad-lang") as Lang)) || "en";
    setLangState(saved);
  }, []);
  const setLang = (l: Lang) => {
    setLangState(l);
    try { localStorage.setItem("fursad-lang", l); } catch {}
  };
  return (
    <I18nContext.Provider value={{ lang, setLang, t: fullDict[lang] }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() { return useContext(I18nContext); }
